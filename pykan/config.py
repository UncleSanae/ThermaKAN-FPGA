import torch

class Config:
    """
    =======================================================================
    全局配置中心 (Configuration Center)
    =======================================================================
    面向 C++/MATLAB 用户的学习指南：
    1. 在 Python 中，类 (class) 可以不实例化，直接当作命名空间 (Namespace) 使用。
       你可以直接用 Config.DEVICE 来访问变量，这类似于 C++ 中的 static 静态成员变量。
    2. 将所有的超参数集中在这里，是为了方便未来做硬件参数扫描或一键调节，
       避免在多个 .py 文件中来回修改导致混乱（避免硬编码）。
    """

    # ---------------------------------------------------------------------
    # 1. 硬件与环境设置
    # ---------------------------------------------------------------------
    # torch.device: PyTorch 特有的硬件指定方式。
    # 逻辑：如果检测到 NVIDIA GPU (CUDA可用)，就会将所有张量 (Tensor) 分配到显存中。
    # 否则回退到 CPU。这比 MATLAB 中手动管理 gpuArray 更加智能和无缝。
    DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    
    # 数据集存放路径
    # 【拓展预留】：如果以后你需要进行多个数据集的交叉对比，可以将这里改为字典或列表
    # 例如：DATA_DIRS = {'test_A': './data_A/', 'test_B': './data_B/'}
    DATA_DIR = './data/'
    
    # ---------------------------------------------------------------------
    # 2. 数据维度与网络拓扑 (重点扩展区)
    # ---------------------------------------------------------------------
    # 时间序列窗口长度 (Sequence Length)
    # 含义：预测当前时刻温度时，向历史回溯的时间步数。
    # 微调：增大该值可以捕捉电池更长期的热惯性，但会增加输入维度和底层硬件查表负担。
    SEQ_LEN = 3  
    
    # 基础物理特征数量 (Base Features)
    # 当前包含：电压(V)、电流(I)、环境温度(T_amb)，共 3 个。
    # 【拓展预留】：如果你未来在硬件系统里加入了新的传感器（例如：电池内阻、外部压力），
    # 只需要将这里的 3 改为对应的数量，并在 data_loader.py 中解析出对应的数据列即可。
    BASE_FEATURES = 3 

    # 输入层维度 (Input Dimension)
    # 含义：基础特征数 × 历史时间步数。
    # 物理映射：在硬件层，这决定了你第一层网络需要多少个并行的输入寄存器接口。
    INPUT_DIM = BASE_FEATURES * SEQ_LEN
    
    # 输出层维度 (Output Dimension)
    # 含义：预测目标的数量。当前仅预测“电池表面温度”这 1 个物理量。
    # 【拓展预留】：如果你想同时预测“表面温度”和“内部核心温度”，将其改为 2。
    # 注意：一旦修改输出维度，你需要同步在 data_loader.py 中提供 2 维的标签 (Label)。
    OUTPUT_DIM = 1
    
    # 网络拓扑结构 (核心架构)
    # 这是一个 Python 列表 (List)，类似于 C++ 的 std::vector。
    # [INPUT_DIM, 64, OUTPUT_DIM] 代表：输入层 -> 64个神经元的隐藏层 -> 输出层。
    # 【拓展预留】：我们的 kan_model.py 被设计为动态级联。如果你要加深网络，
    # 直接在中间添加数字即可，例如 [INPUT_DIM, 64, 32, OUTPUT_DIM]。
    # 底层代码会自动根据这个列表长度生成对应的硬件连线层，无需修改内部逻辑。
    LAYERS = [INPUT_DIM, 64, OUTPUT_DIM] 
    
    # ---------------------------------------------------------------------
    # 3. 训练超参数 (Hyperparameters)
    # ---------------------------------------------------------------------
    # 总训练轮数 (Epochs)
    # 含义：整个数据集被网络完全遍历的次数。
    EPOCHS = 3000
    
    # 批次大小 (Batch Size)
    # 含义：类似于 FPGA 里的数据打包流处理。每次将 64 组数据打包送入网络，
    # 计算完平均梯度后再更新一次权重。较小的 Batch Size 包含更多“噪声”，
    # 有助于网络跳出局部最优解，提升在未知工况下的泛化能力。
    BATCH_SIZE = 64
    
    # ---------------------------------------------------------------------
    # 4. 优化器与防溢出控制 (双线独立调节)
    # ---------------------------------------------------------------------
    # 概念对标：PyTorch 的优化器(Optimizer)负责在反向传播计算出梯度后，实际更新权重。
    # KAN 架构分为 Base(基础线性路径) 和 Spline(样条非线性路径)，两者数学特性差异极大，必须独立配置。

    # 基础路径学习率 (Base Learning Rate)
    # 含义：控制 Base_Weight 的更新步长。
    LR_BASE = 1e-3
    
    # 基础路径权重衰减 (Weight Decay / L2 正则化)
    # 物理映射：这是硬件定点数防溢出的第一道防线。它会在 Loss 中加入对权重绝对值的惩罚，
    # 逼迫 Base_Weight 向 0 靠拢。如果提取到 CSV 的权重超出了 Q1.15 量程，将其调大。
    WEIGHT_DECAY_BASE = 1e-4
    
    # 样条路径学习率 (Spline Learning Rate)
    # 含义：控制 B-样条控制点 (Spline_0 ~ Spline_10) 的更新步长。
    # 样条需要更大的灵活性去拟合高频物理突变，因此通常设置得比 LR_BASE 大。
    LR_SPLINE = 5e-3
    
    # 样条路径权重衰减
    # 物理映射：设为 0.0 是为了让 B-样条函数保持最大自由度去拟合非线性。
    # 仅当样条权重在硬件中引起加法树溢出时，才考虑赋予极小的惩罚 (如 1e-5)。
    WEIGHT_DECAY_SPLINE = 0.0
    
    # 梯度裁剪阈值 (Gradient Clipping)
    # 含义：反向传播时，如果算出的梯度向量的 L2 范数超过 1.0，强制等比例缩小。
    # 这是深度学习中防止训练初期“梯度爆炸”（数值变成 NaN）的标准硬防护。
    MAX_GRAD_NORM = 1.0
    
    # ---------------------------------------------------------------------
    # 5. 评估与测试机制
    # ---------------------------------------------------------------------
    # 留出测试集的文件数量
    # 含义：从 data/ 目录下随机抽取 5 个真实的 .mat 文件，它们绝不参与训练更新，
    # 纯粹用于在训练结束后，检验模型的硬件模拟推理精度。
    TEST_FILES_COUNT = 5